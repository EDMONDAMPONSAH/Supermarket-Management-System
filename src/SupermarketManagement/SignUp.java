    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        chooser.showOpenDialog(null);
        File f = chooser.getSelectedFile();
        filepath = f.getAbsolutePath();
        ImageIcon img = new ImageIcon(new ImageIcon(filepath).getImage().getScaledInstance(jLabel12.getWidth(), jLabel12.getHeight(), Image.SCALE_SMOOTH));
        jLabel12.setIcon(img);

        try {
            File image = new File(filepath);
            FileInputStream fis = new FileInputStream(image);
            ByteArrayOutputStream bos = new ByteArrayOutputStream();
            byte[] buf = new byte[1024];
            for (int number; (number = fis.read(buf)) != -1;) {
                bos.write(buf, 0, number);
            }
            person_image = bos.toByteArray();
        } catch (FileNotFoundException e) {
            JOptionPane.showMessageDialog(null, e);
        } catch (IOException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String idd = jLabel13.getText();
        String phone = jTextField2.getText();
        String email = jTextField3.getText();
        String password = jPasswordField1.getText();
        String role = jComboBox1.getSelectedItem().toString();
        SimpleDateFormat sf = new SimpleDateFormat("dd-MM-yyyy");
        Date d = jDateChooser1.getDate();
        String dob = "";
        if (d != null) {
            dob = sf.format(jDateChooser1.getDate());

        }

        if (name.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Name Field is blank</b><html>", "Message", JOptionPane.ERROR_MESSAGE);

        } else if (phone.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Phone Field is blank</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (phone.length() != 10) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Invalid Phone Number</b><html>", "Message", JOptionPane.ERROR_MESSAGE);

        } else if (email.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Email Field is blank</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Invalid Email Address</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (password.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Password Field is blank</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (dob.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Date of Birth Field is blank</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (gender == null) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Select Gender</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (role.equals("choose role")) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Select your role</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else if (jLabel12.getIcon() == null) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Attach your Picture</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                con = database.connectDB();
                pst = con.prepareStatement("insert into userss(name,phone,email,password,gender,dob,image,role,idd,status)values(?,?,?,?,?,?,?,?,?,?)");
                pst.setString(1, name);
                pst.setString(2, phone);
                pst.setString(3, email);
                pst.setString(4, password);
                pst.setString(5, gender);
                pst.setString(6, dob);
                pst.setBytes(7, person_image);
                pst.setString(8, role);
                pst.setString(9, idd);
                pst.setString(10, "inactive");
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;\">Account Created Successfully.</b><html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                autoID();
                clearFields();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField3KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField3KeyReleased
        // TODO add your handling code here:
        try{
            String email=jTextField3.getText();
            alreadyExist=0;
            con=database.connectDB();
            pst=con.prepareStatement("select * from userss where email = ?");
            pst.setString(1, email);
            rs=pst.executeQuery();
            while(rs.next()){
                alreadyExist=1;
                jLabel14.setVisible(true);
                jButton1.setEnabled(false);
            }
            if(alreadyExist==0){
                jLabel14.setVisible(false);
                jButton1.setEnabled(true);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        finally{
            try{
                pst.close();
                rs.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jTextField3KeyReleased

