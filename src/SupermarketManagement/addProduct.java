    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String qty = jTextField2.getText();
        String ppu = jTextField3.getText();
        if (name.isBlank() || qty.isBlank() || ppu.isBlank()) {
            JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;color:red;\">Fill blank fields</b><html>", "Message", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                con = database.connectDB();
                pst = con.prepareStatement("insert into productss(idd,name,qty,ppu)values(?,?,?,?)");
                pst.setString(1, jLabel3.getText());
                pst.setString(2, name);
                pst.setString(3, qty);
                pst.setString(4, ppu);
                pst.executeUpdate();
                Linear barcode = new Linear();
                barcode.setType(Linear.CODE128B);
                barcode.setData(jLabel3.getText());
                barcode.setI(11.0f);
                String fname = jLabel3.getText();
                barcode.renderBarcode("C:\\Users\\user\\Desktop" + fname + ".png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"font-size:12px;\">Product Successfully Saved</b><html>", "Message", JOptionPane.INFORMATION_MESSAGE);
                autoID();
                clearFields();

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    pst.close();
                    rs.close();
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


