    private void autoID() {
        try {
            con = database.connectDB();
            st = con.createStatement();
            rs = st.executeQuery("select max(idd) from productss");
            rs.next();
            rs.getString("max(idd)");
            if (rs.getString("max(idd)") == null) {
                jLabel3.setText("90001");
            } else {
                long id = Long.parseLong(rs.getString("max(idd)").substring(1, rs.getString("max(idd)").length()));
                id++;
                jLabel3.setText("9" + String.format("%04d", id));

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                st.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

