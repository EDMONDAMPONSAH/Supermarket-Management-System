    private void autoID() {
        try {
            con = database.connectDB();
            st = con.createStatement();
            rs = st.executeQuery("select max(idd) from userss");
            rs.next();
            rs.getString("max(idd)");
            if (rs.getString("max(idd)") == null) {
                jLabel13.setText("EP001");
            } else {
                long id = Long.parseLong(rs.getString("max(idd)").substring(2, rs.getString("max(idd)").length()));
                id++;
                jLabel13.setText("EP" + String.format("%03d", id));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                st.close();
                rs.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

